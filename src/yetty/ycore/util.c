size_t yetty_core_base64_decode(const char *in, size_t in_len, char *out, size_t out_cap)
{
    size_t out_len = 0;
    int val = 0, valb = -8;

    for (size_t i = 0; i < in_len && out_len < out_cap; i++) {
        int c = b64_table[(unsigned char)in[i]];
        if (c == -1)
            break;
        val = (val << 6) + c;
        valb += 6;
        if (valb >= 0) {
            out[out_len++] = (char)((val >> valb) & 0xFF);
            valb -= 8;
        }
    }
    return out_len;
}
