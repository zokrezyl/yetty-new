//=============================================================================
// Factory
//=============================================================================

Result<Yetty::Ptr> Yetty::createImpl(ContextType&, Config::Ptr config) noexcept {
    auto impl = Ptr(new YettyImpl());
    if (auto res = static_cast<YettyImpl*>(impl.get())->init(config); !res) {
        yerror("Yetty creation failed: {}", error_msg(res));
        return Err<Ptr>("Failed to init Yetty", res);
    }
    ytest("yetty-created", "Yetty created successfully");
    return Ok(std::move(impl));
}

//=============================================================================
// Initialization
//=============================================================================

Result<void> YettyImpl::init(Config::Ptr config) noexcept {
    ydebug("Yetty starting...");

    // Store config
    _yettyContext.config = config;

    // Read settings from config
    _vncClientMode = config->get<bool>("vnc/client-mode", false);
    _vncServerMode = config->get<bool>("vnc/server-mode", false);
    _vncHeadless = config->get<bool>("vnc/headless", false);
    _vncHost = config->get<std::string>("vnc/host", "");
    _vncPort = static_cast<uint16_t>(config->get<int>("vnc/port", 5900));
    _vncServerPort = static_cast<uint16_t>(config->get<int>("vnc/server-port", 5900));
    _vncMergeRects = config->get<bool>("vnc/merge-rects", false);
    _vncForceRaw = config->get<bool>("vnc/force-raw", false);
    _vncCompressionQuality = static_cast<uint8_t>(config->get<int>("vnc/compression-quality", 0));
    _vncAlwaysFull = config->get<bool>("vnc/always-full", false);
    _vncUseH264 = config->get<bool>("vnc/use-h264", false);
    _vncTestMode = config->get<bool>("vnc/test-mode", false);
    _vncTestPattern = config->get<std::string>("vnc/test-pattern", "text");
    _captureBenchmark = config->get<bool>("debug/capture-benchmark", false);
    _msdfProviderName = config->get<std::string>("rendering/msdf-provider", "gpu");
    _executeCommand = config->get<std::string>("shell/command", "");
    _telnetAddress = config->get<std::string>("shell/telnet", "");
    _telnetClientMode = !_telnetAddress.empty();

    ydebug("init: initWindow");
    if (auto res = initWindow(); !res) { ydebug("init: initWindow FAILED"); return res; }

    // Extract embedded assets if needed
    ydebug("init: initEmbeddedAssets");
    if (auto res = initEmbeddedAssets(); !res) {
        ywarn("init: initEmbeddedAssets failed (continuing with fallback): {}", res.error().message());
    }

    // Get EventQueue for thread-safe GPU callback wakeups
    auto queueResult = base::EventQueue::instance();
    if (!queueResult) {
        return Err<void>("EventQueue not available", queueResult);
    }
    _eventQueue = *queueResult;

    ydebug("init: initWebGPU");
    if (auto res = initWebGPU(); !res) { ydebug("init: initWebGPU FAILED"); return res; }
    ydebug("init: initSharedResources");
    if (auto res = initSharedResources(); !res) { ydebug("init: initSharedResources FAILED"); return res; }

    // Get paths from config (paths are platform-specific defaults, can be overridden)
    auto shadersDir = _yettyContext.config->get<std::string>("paths/shaders", "");
    auto fontsDir = _yettyContext.config->get<std::string>("paths/fonts", "");
    auto msdfFontsDir = _yettyContext.config->get<std::string>("paths/msdf-fonts", "");

    // Create ShaderManager with GPUContext, allocator, and shaders directory from config
    ydebug("init: ShaderManager::create");
    auto shaderMgrResult = ShaderManager::create(_gpuContext, _gpuAllocator, shadersDir);
    if (!shaderMgrResult) {
        ydebug("init: ShaderManager::create FAILED");
        return Err<void>("Failed to create ShaderManager", shaderMgrResult);
    }
    auto shaderMgr = *shaderMgrResult;
    ydebug("init: ShaderManager created");

    // Create MSDF CDB provider based on CLI flag
    MsdfCdbProvider::Ptr cdbProvider;
#if !YETTY_WEB
    ydebug("init: MSDF CDB provider");
    if (_msdfProviderName == "cpu") {
        cdbProvider = std::make_shared<CpuMsdfCdbProvider>();
        ydebug("init: Using CPU MSDF CDB provider");
    } else {
        cdbProvider = std::make_shared<GpuMsdfCdbProvider>(_instance, shadersDir);
        ydebug("init: Using GPU MSDF CDB provider");
    }
#endif

    // Create FontManager with GPUContext, ShaderManager, and directories from config
    ydebug("init: FontManager::create");

    // Extract shader preload lists from config
    std::vector<std::string> preloadCardShaders;
    std::vector<std::string> preloadGlyphShaders;
    if (_yettyContext.config) {
        preloadCardShaders = _yettyContext.config->getPathList("shaders/preload/cards");
        preloadGlyphShaders = _yettyContext.config->getPathList("shaders/preload/glyphs");
        ydebug("init: shader preload - {} cards, {} glyphs",
              preloadCardShaders.size(), preloadGlyphShaders.size());
    }

    auto fontMgrResult = FontManager::create(_gpuContext, _gpuAllocator, shaderMgr, msdfFontsDir, fontsDir, shadersDir, cdbProvider, preloadCardShaders, preloadGlyphShaders);
    if (!fontMgrResult) {
        ydebug("init: FontManager::create FAILED");
        return Err<void>("Failed to create FontManager", fontMgrResult);
    }
    auto fontMgr = *fontMgrResult;
    ydebug("init: FontManager created");

    // Build YettyContext
    _yettyContext.gpu = _gpuContext;
    _yettyContext.gpuAllocator = _gpuAllocator;
    // SurfaceManager and ClipboardManager are singletons - access via ::instance() when needed
    // Paths are in config under "paths/*" - can be overridden via config file or env vars
#if !YETTY_WEB && !YETTY_IOS && !defined(__ANDROID__)
    _yettyContext.gpuMonitor = gpu::GpuMonitor::create();
#endif
    _yettyContext.shaderManager = shaderMgr;
    _yettyContext.fontManager = fontMgr;

    // Create YGuiOverlay
    auto yguiResult = YGuiOverlay::create(_yettyContext);
    if (!yguiResult) {
        ywarn("Failed to create YGuiOverlay: {}", yguiResult.error().message());
    } else {
        _yettyContext.yguiOverlay = *yguiResult;
        _yettyContext.yguiOverlay->updateDisplaySize(_surfaceWidth, _surfaceHeight);
    }

    // Create CardFactory (card types registry, no CardBufferManager needed)
    {
        auto cardFactoryResult = CardFactory::create(_gpuContext);
        if (!cardFactoryResult) {
            return Err<void>("Failed to create CardFactory", cardFactoryResult);
        }
        _yettyContext.cardFactory = *cardFactoryResult;
    }

    // Register plot providers with ShaderManager
    card::PlotSamplerProvider::instance()->registerWith(shaderMgr);
    card::PlotTransformerProvider::instance()->registerWith(shaderMgr);
    card::PlotRendererProvider::instance()->registerWith(shaderMgr);

    // Compile shaders after all providers (fonts, plot) are registered
    ydebug("init: compiling shaders");
    if (auto res = shaderMgr->compile(); !res) {
        ydebug("init: shader compile FAILED");
        return Err<void>("Failed to compile shaders", res);
    }
    ydebug("init: shaders compiled");

    initEventLoop();

#if !YETTY_WEB && !YETTY_IOS && !defined(__ANDROID__)
    // Create RPC server (config already has rpc/enabled and rpc/socket-path set)
    if (_yettyContext.config->get<bool>("rpc/enabled", false)) {
        auto socketPath = _yettyContext.config->get<std::string>("rpc/socket-path", "");
        auto rpcResult = rpc::RpcServer::create(socketPath);
        if (!rpcResult) {
            return Err<void>("Failed to create RPC server", rpcResult);
        }
        _rpcServer = *rpcResult;
        _yettyContext.config->setString("shell/env/YETTY_SOCKET", socketPath);
        rpc::registerEventLoopHandlers(*_rpcServer);
    }
#endif

    // Skip workspace/PTY creation in VNC client mode only - we just display remote content
    // Telnet mode DOES need a workspace with terminal (uses TelnetPtyReader)
    if (!_vncClientMode) {
        ydebug("init: initWorkspace");
        if (auto res = initWorkspace(); !res) { ydebug("init: initWorkspace FAILED"); return res; }
        ydebug("init: initWorkspace done");
    }

#if !YETTY_WEB && !YETTY_IOS && !defined(__ANDROID__)
    // Register workspace handlers now that workspace exists
    if (_rpcServer) {
        if (_activeWorkspace) {
            rpc::registerWorkspaceHandlers(*_rpcServer, _activeWorkspace);
        }

        // Register stream handlers (uses GPUScreenManager singleton)
        rpc::registerStreamHandlers(*_rpcServer);

        // Register integration test handlers (get_cell_size, get_card_info, etc.)
        rpc::registerTestHandlers(*_rpcServer);

        if (auto res = _rpcServer->start(); !res) {
            return Err<void>("Failed to start RPC server", res);
        }
        ydebug("RPC server started on {}", _rpcServer->socketPath());
    }
#endif

    s_instance = this;
    if (!_vncHeadless) {
        if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
            _lastFpsTime = (*surfaceResult)->getTime();
        }
    }

    // Initialize capture benchmark mode if enabled
    if (_captureBenchmark) {
        _jpegCompressor = tjInitCompress();
        if (!_jpegCompressor) {
            return Err<void>("Failed to initialize TurboJPEG compressor");
        }
        ydebug("Capture benchmark: TurboJPEG compressor initialized");
    }

    // Initialize VNC client mode if enabled
    if (_vncClientMode) {
        // Get initial VNC content area size (window minus our statusbar)
        int windowW = 800, windowH = 600;
        if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
            (*surfaceResult)->getWindowSize(windowW, windowH);
        }
        float statusbarH = _yettyContext.yguiOverlay ? _yettyContext.yguiOverlay->getStatusbarHeight() : 0.0f;
        int vncH = windowH - static_cast<int>(statusbarH);
        uint16_t vncWidth = static_cast<uint16_t>(windowW > 0 ? windowW : 800);
        uint16_t vncHeight = static_cast<uint16_t>(vncH > 0 ? vncH : 600);

        // Create VNC client with initial size - texture created immediately
        _vncClient = std::make_shared<vnc::VncClient>(_device, _queue, _surfaceFormat, vncWidth, vncHeight);

        // Set up callback for when connection completes (async or immediate)
        // The resize MUST be sent AFTER connect completes, not before!
        _vncClient->onConnected = [this, vncWidth, vncHeight]() {
            ydebug("VNC client connected - sending initial resize {}x{}", vncWidth, vncHeight);
            _vncClient->sendResize(vncWidth, vncHeight);
            _vncClient->sendCellSize(20);  // Default cell height
            // Send compression config if custom settings are specified
            if (_vncForceRaw || _vncCompressionQuality > 0 || _vncAlwaysFull || _vncUseH264) {
                uint8_t codec = _vncUseH264 ? vnc::CODEC_H264 : vnc::CODEC_JPEG;
                _vncClient->sendCompressionConfig(_vncForceRaw, _vncCompressionQuality, _vncAlwaysFull, codec);
            }
        };

        // Set up frame received callback for immediate screen refresh
        _vncClient->onFrameReceived = [this]() {
#if YETTY_WEB
            // Web builds: request animation frame directly
            if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
                (*surfaceResult)->requestRender();
            }
#else
            auto t = std::chrono::high_resolution_clock::now();
            auto ms = std::chrono::duration_cast<std::chrono::milliseconds>(t.time_since_epoch()).count();
            ydebug("[TIME] CLIENT onFrameReceived dispatching ScreenUpdate at {}ms", ms);
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) return;
            (*loopResult)->dispatch(base::Event::screenUpdateEvent());
#endif
        };

        // Set up reconnection parameters
        _vncClient->setReconnectParams(_vncHost, _vncPort);

        // Set up disconnection callback for reconnection
        _vncClient->onDisconnected = [this]() {
            ydebug("VNC client disconnected, will attempt reconnection...");
        };

        // Initial connection attempt - don't fail if it doesn't connect immediately
        // The main loop will handle reconnection attempts
        if (auto res = _vncClient->connect(_vncHost, _vncPort); !res) {
            ywarn("Initial VNC connection failed: {} - will retry", res.error().message());
            // Mark as wanting reconnect so main loop will retry
        }
        ydebug("VNC client mode initialized for {}:{}", _vncHost, _vncPort);
    }

#if YETTY_WEB
    // Telnet client mode: the workspace was already initialized with telnet config
    // TelnetPtyReader will use TelnetClient which now has WebSocket support
    if (_telnetClientMode) {
        ydebug("Telnet client mode initialized for: {}", _telnetAddress);
    }
#endif

    // Initialize VNC server mode if enabled
    if (_vncServerMode) {
        _vncServer = std::make_shared<vnc::VncServer>(_device, _queue);
        if (_vncMergeRects) {
            _vncServer->setMergeRectangles(true);
        }
        if (_vncCompressionQuality > 0) {
            _vncServer->setJpegQuality(_vncCompressionQuality);
        }
        if (auto res = _vncServer->start(_vncServerPort); !res) {
            return Err<void>("Failed to start VNC server", res);
        }
        ydebug("VNC server started on port {}", _vncServerPort);

        // Set up input callbacks from VNC clients
        _vncServer->onMouseMove = [](int16_t x, int16_t y, uint8_t mods) {
            ydebug("VNC onMouseMove callback: x={} y={} mods={}", x, y, mods);
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) {
                ywarn("VNC onMouseMove: EventLoop::instance() failed!");
                return;
            }
            ydebug("VNC onMouseMove: dispatching to EventLoop");
            (*loopResult)->dispatch(base::Event::mouseMove(static_cast<float>(x), static_cast<float>(y)));
        };

        _vncServer->onMouseButton = [](int16_t x, int16_t y, vnc::MouseButton button, bool pressed, uint8_t mods) {
            ydebug("VNC onMouseButton callback: x={} y={} btn={} pressed={} mods={}", x, y, static_cast<int>(button), pressed, mods);
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) {
                ywarn("VNC onMouseButton: EventLoop::instance() failed!");
                return;
            }
            auto loop = *loopResult;
            int btn = static_cast<int>(button);
            if (pressed) {
                ydebug("VNC onMouseButton: dispatching mouseDown");
                loop->dispatch(base::Event::mouseDown(static_cast<float>(x), static_cast<float>(y), btn, mods));
            } else {
                ydebug("VNC onMouseButton: dispatching mouseUp");
                loop->dispatch(base::Event::mouseUp(static_cast<float>(x), static_cast<float>(y), btn, mods));
            }
        };

        _vncServer->onMouseScroll = [](int16_t x, int16_t y, int16_t dx, int16_t dy, uint8_t mods) {
            ydebug("VNC onMouseScroll callback: x={} y={} dx={} dy={} mods={}", x, y, dx, dy, mods);
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) {
                ywarn("VNC onMouseScroll: EventLoop::instance() failed!");
                return;
            }
            // VNC protocol uses 120 units per scroll notch, normalize to 1.0
            float normDx = static_cast<float>(dx) / 120.0f;
            float normDy = static_cast<float>(dy) / 120.0f;
            ydebug("VNC onMouseScroll: dispatching to EventLoop (normalized dx={} dy={})", normDx, normDy);
            (*loopResult)->dispatch(base::Event::scrollEvent(
                static_cast<float>(x), static_cast<float>(y),
                normDx, normDy, mods));
        };

        _vncServer->onKeyDown = [](uint32_t keycode, uint32_t scancode, uint8_t mods) {
            ydebug("VNC onKeyDown callback: keycode={} scancode={} mods={}", keycode, scancode, mods);
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) {
                ywarn("VNC onKeyDown: EventLoop::instance() failed!");
                return;
            }
            auto loop = *loopResult;

            // Handle Ctrl/Alt + character combinations - dispatch as charInputWithMods
            // to match local keyboard handling behavior
            // VNC mods: MOD_CTRL=0x02, MOD_ALT=0x04
            if (mods & (0x02 | 0x04)) {
                uint32_t ch = 0;
                // GLFW key codes: SPACE=32, 0-9=48-57, A-Z=65-90
                if (keycode == 32) {
                    // Space - dispatch as keyDown
                    loop->dispatch(base::Event::keyDown(static_cast<int>(keycode), static_cast<int>(mods), static_cast<int>(scancode)));
                    return;
                } else if (keycode >= 48 && keycode <= 57) {
                    // 0-9
                    ch = keycode;
                } else if (keycode >= 65 && keycode <= 90) {
                    // A-Z -> a-z (lowercase)
                    ch = keycode + 32;
                }
                if (ch != 0) {
                    ydebug("VNC onKeyDown: Ctrl/Alt+char -> charInputWithMods ch={} mods={}", ch, mods);
                    loop->dispatch(base::Event::charInputWithMods(ch, static_cast<int>(mods)));
                    return;
                }
            }

            // For special keys (Enter, arrows, etc.), dispatch keyDown
            ydebug("VNC onKeyDown: dispatching keyDown to EventLoop");
            loop->dispatch(base::Event::keyDown(static_cast<int>(keycode), static_cast<int>(mods), static_cast<int>(scancode)));
        };

        _vncServer->onKeyUp = [](uint32_t keycode, uint32_t scancode, uint8_t mods) {
            ydebug("VNC onKeyUp callback: keycode={} scancode={} mods={}", keycode, scancode, mods);
            // Key up events are not typically used by terminal
        };

        _vncServer->onTextInput = [](const std::string& text) {
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) return;
            auto loop = *loopResult;
            // Process each codepoint in the UTF-8 string
            for (size_t i = 0; i < text.size(); ) {
                uint32_t cp = 0;
                uint8_t c = text[i];
                if ((c & 0x80) == 0) {
                    cp = c;
                    i += 1;
                } else if ((c & 0xE0) == 0xC0 && i + 1 < text.size()) {
                    cp = ((c & 0x1F) << 6) | (text[i+1] & 0x3F);
                    i += 2;
                } else if ((c & 0xF0) == 0xE0 && i + 2 < text.size()) {
                    cp = ((c & 0x0F) << 12) | ((text[i+1] & 0x3F) << 6) | (text[i+2] & 0x3F);
                    i += 3;
                } else if ((c & 0xF8) == 0xF0 && i + 3 < text.size()) {
                    cp = ((c & 0x07) << 18) | ((text[i+1] & 0x3F) << 12) | ((text[i+2] & 0x3F) << 6) | (text[i+3] & 0x3F);
                    i += 4;
                } else {
                    i++;  // Skip invalid
                    continue;
                }
                loop->dispatch(base::Event::charInput(cp));
            }
        };

        // VNC client resize - changes the terminal grid size (not cell size)
        _vncServer->onResize = [this](uint16_t widthPx, uint16_t heightPx) {
            if (widthPx == 0 || heightPx == 0) return;

            // Special value 0xFFFF x 0xFFFF means "use current window size"
            // Used by recording clients that don't want to change the terminal size
            if (widthPx == 0xFFFF && heightPx == 0xFFFF) {
                if (_vncRequestedWidth > 0 && _vncRequestedHeight > 0) {
                    ydebug("VNC onResize: client requested current size, already {}x{}",
                           _vncRequestedWidth, _vncRequestedHeight);
                    return;
                }
                // Use current window dimensions (or defaults in headless mode)
                int w = 800, h = 600;
                if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
                    (*surfaceResult)->getFramebufferSize(w, h);
                }
                widthPx = static_cast<uint16_t>(w);
                heightPx = static_cast<uint16_t>(h);
                ydebug("VNC onResize: client requested current size -> {}x{}", widthPx, heightPx);
                _vncRequestedWidth = widthPx;
                _vncRequestedHeight = heightPx;
                _vncServer->forceFullFrame();
                return;
            }

            ydebug("VNC onResize: {}x{} px", widthPx, heightPx);

            // Store requested capture size (full VNC area including server's statusbar)
            _vncRequestedWidth = widthPx;
            _vncRequestedHeight = heightPx;

            // Resize workspace to client area minus server's statusbar
            // Server renders terminal in top portion, statusbar at bottom
            if (_activeWorkspace) {
                float statusbarH = _yettyContext.yguiOverlay ? _yettyContext.yguiOverlay->getStatusbarHeight() : 0.0f;
                float wsH = std::max(1.0f, static_cast<float>(heightPx) - statusbarH);
                _activeWorkspace->resize(static_cast<float>(widthPx), wsH);
                ydebug("VNC onResize: workspace={}x{} (statusbar={})", widthPx, wsH, statusbarH);
            }

            // Update ygui overlay display size for statusbar positioning
            if (_yettyContext.yguiOverlay) {
                _yettyContext.yguiOverlay->updateDisplaySize(widthPx, heightPx);
            }

            // Force full frame on next capture
            _vncServer->forceFullFrame();
        };

        // VNC client cell size change (zoom)
        _vncServer->onCellSize = [this](uint8_t cellHeight) {
            ydebug("VNC onCellSize: cellHeight={}", cellHeight);
            _vncServer->forceFullFrame();
        };

        // VNC character with modifiers (layout-mapped, for Ctrl/Alt + character)
        _vncServer->onCharWithMods = [](uint32_t codepoint, uint8_t mods) {
            ydebug("VNC onCharWithMods: codepoint={} ('{}') mods={}", codepoint, (char)codepoint, mods);
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) return;
            (*loopResult)->dispatch(base::Event::charInputWithMods(codepoint, static_cast<int>(mods)));
        };

        // VNC input received - trigger immediate screen refresh
        _vncServer->onInputReceived = []() {
            auto t = std::chrono::high_resolution_clock::now();
            auto ms = std::chrono::duration_cast<std::chrono::milliseconds>(t.time_since_epoch()).count();
            ydebug("[TIME] onInputReceived lambda called at {}ms", ms);
            auto loopResult = base::EventLoop::instance();
            if (!loopResult) return;
            (*loopResult)->dispatch(base::Event::screenUpdateEvent());
        };
    }

    return Ok();
}

Result<void> YettyImpl::initEmbeddedAssets() noexcept {
    ydebug("initEmbeddedAssets: starting");

    // Get IncbinAssets singleton
    auto assetsResult = IncbinAssets::instance();
    if (!assetsResult) {
        // No embedded assets - this is fine for development builds
        ydebug("No embedded assets available (development build)");
        return Ok();
    }
    auto assets = *assetsResult;

    // List embedded assets
    auto assetList = assets->list();
    ydebug("initEmbeddedAssets: {} assets registered", assetList.size());
    for (const auto& name : assetList) {
        ydebug("initEmbeddedAssets:   - '{}'", name);
    }
    if (assetList.empty()) {
        ydebug("No embedded assets");
        return Ok();
    }

    // Derive cache base directory from config paths/shaders
    // Config returns full paths like "~/.cache/yetty/shaders" - we need the parent
    std::filesystem::path shadersPath = _yettyContext.config->get<std::string>("paths/shaders", "");
    std::filesystem::path cacheDir = shadersPath.parent_path();
    ydebug("initEmbeddedAssets: cacheDir={}", cacheDir.string());

    // Check if extraction is needed: version changed OR any file is missing
    bool needsExtraction = IncbinAssets::needsExtraction(cacheDir);
    ydebug("initEmbeddedAssets: needsExtraction (marker check) = {}", needsExtraction);

    if (!needsExtraction) {
        // Version marker exists and matches - but check if any files are missing
        ydebug("initEmbeddedAssets: marker OK, checking if files exist...");
        for (const auto& name : assetList) {
            auto filePath = cacheDir / name;
            bool exists = std::filesystem::exists(filePath);
            ydebug("initEmbeddedAssets:   {} -> exists={}", filePath.string(), exists);
            if (!exists) {
                ytest("assets-file-missing", "Missing asset file: %s - will re-extract", name.c_str());
                needsExtraction = true;
                break;
            }
        }
    }

    ydebug("initEmbeddedAssets: final needsExtraction = {}", needsExtraction);
    if (!needsExtraction) {
        ytest("assets-already-extracted", "Embedded assets already extracted to %s", cacheDir.string().c_str());
        return Ok();
    }

    // Extract assets
    ytest("assets-extracting", "Extracting embedded assets to %s", cacheDir.string().c_str());
    if (auto res = assets->extractTo(cacheDir); !res) {
        return Err<void>("Failed to extract embedded assets", res);
    }

    ydebug("initEmbeddedAssets: extraction complete");

    // Install default config to XDG config path if it doesn't exist
    // This only happens on first run when no user config is present
    auto xdgConfigPath = Config::getXDGConfigPath();
    if (!xdgConfigPath.empty() && !std::filesystem::exists(xdgConfigPath)) {
        auto defaultConfigSrc = cacheDir / "default-config.yaml";
        if (std::filesystem::exists(defaultConfigSrc)) {
            // Create config directory if needed
            auto configDir = xdgConfigPath.parent_path();
            if (!configDir.empty()) {
                std::error_code ec;
                std::filesystem::create_directories(configDir, ec);
                if (ec) {
                    ywarn("Failed to create config directory {}: {}", configDir.string(), ec.message());
                } else {
                    // Copy default config to user config location
                    std::filesystem::copy_file(defaultConfigSrc, xdgConfigPath, ec);
                    if (ec) {
                        ywarn("Failed to install default config: {}", ec.message());
                    } else {
                        yinfo("Installed default config to {}", xdgConfigPath.string());
                    }
                }
            }
        }
    }

    return Ok();
}

Result<void> YettyImpl::initWindow() noexcept {
    // In headless mode, nothing to initialize here
    if (_vncHeadless) {
        ydebug("VNC headless mode: no window initialization");
        return Ok();
    }

    // Get window size from SurfaceManager (singleton, already created by InitManager)
    auto surfaceResult = SurfaceManager::instance();
    if (!surfaceResult) {
        return Err<void>("SurfaceManager not available", surfaceResult);
    }
    auto surface = *surfaceResult;

    // Set window icon
    surface->setIcon(gLogoData, gLogoSize);

    // Get actual window size
    int actualW, actualH;
    surface->getWindowSize(actualW, actualH);
    if (actualW > 0 && actualH > 0) {
        _initialWidth = static_cast<uint32_t>(actualW);
        _initialHeight = static_cast<uint32_t>(actualH);
    }

    ydebug("Window initialized, size {}x{}", actualW, actualH);
    return Ok();
}

Result<void> YettyImpl::initWebGPU() noexcept {
    ydebug("initWebGPU: Creating instance...");

    // Create instance
    WGPUInstanceDescriptor instanceDesc = {};
    _instance = wgpuCreateInstance(&instanceDesc);
    if (!_instance) {
        return Err<void>("Failed to create WebGPU instance");
    }
    ydebug("initWebGPU: Instance created");

    // Create surface (skip in headless mode)
    if (!_vncHeadless) {
        auto surfaceResult = SurfaceManager::instance();
        if (surfaceResult) {
            auto surface = *surfaceResult;
            _surface = surface->createWGPUSurface(_instance);
            if (_surface) {
                ydebug("initWebGPU: Surface created");
            } else {
                ywarn("initWebGPU: Surface creation failed - will try without surface");
            }

            // Initialize content scale
            int fbWidth, fbHeight, winWidth, winHeight;
            surface->getFramebufferSize(fbWidth, fbHeight);
            surface->getWindowSize(winWidth, winHeight);
            if (winWidth > 0 && winHeight > 0) {
                _contentScaleX = static_cast<float>(fbWidth) / static_cast<float>(winWidth);
                _contentScaleY = static_cast<float>(fbHeight) / static_cast<float>(winHeight);
                ydebug("Content scale: {}x{}", _contentScaleX, _contentScaleY);
            }
        }
    } else {
        ydebug("initWebGPU: Headless mode - skipping surface creation");
    }

    // Request adapter (without compatibleSurface if headless or surface creation failed)
    WGPURequestAdapterOptions adapterOpts = {};
    adapterOpts.compatibleSurface = _surface;  // nullptr if headless or surface failed
    adapterOpts.powerPreference = WGPUPowerPreference_HighPerformance;

#ifdef __ANDROID__
    // On Android, explicitly request Vulkan backend (required for SwiftShader in emulator)
    adapterOpts.backendType = WGPUBackendType_Vulkan;
    ydebug("initWebGPU: Requesting Vulkan backend on Android");
#endif

    WGPURequestAdapterCallbackInfo adapterCallbackInfo = {};
    adapterCallbackInfo.mode = WGPUCallbackMode_AllowSpontaneous;
    adapterCallbackInfo.callback = [](WGPURequestAdapterStatus status, WGPUAdapter adapter,
                                      WGPUStringView message, void* userdata1, void* userdata2) {
        if (status == WGPURequestAdapterStatus_Success) {
            *static_cast<WGPUAdapter*>(userdata1) = adapter;
        }
    };
    adapterCallbackInfo.userdata1 = &_adapter;

    ydebug("initWebGPU: Requesting adapter...");
    wgpuInstanceRequestAdapter(_instance, &adapterOpts, adapterCallbackInfo);

#if defined(__EMSCRIPTEN__)
    // On Emscripten, WebGPU operations are async - poll until callback fires
    while (!_adapter) {
        emscripten_sleep(1);
    }
#endif

#ifdef _WIN32
    // On Windows, if default adapter request failed or returned software/WARP renderer,
    // try D3D11 backend which supports older GPUs (pre-D3D12/Vulkan hardware)
    if (_adapter) {
        WGPUAdapterInfo info = {};
        if (wgpuAdapterGetInfo(_adapter, &info) == WGPUStatus_Success) {
            // Check if we got WARP (software) - adapterType 3 = CPU/Software
            if (info.adapterType == WGPUAdapterType_CPU) {
                ydebug("initWebGPU: Got software renderer, trying D3D11 backend for hardware GPU...");
                wgpuAdapterRelease(_adapter);
                _adapter = nullptr;
                adapterOpts.backendType = WGPUBackendType_D3D11;
                wgpuInstanceRequestAdapter(_instance, &adapterOpts, adapterCallbackInfo);
                // If D3D11 also fails, fall back to whatever works
                if (!_adapter) {
                    ywarn("initWebGPU: D3D11 backend unavailable, falling back to any adapter");
                    adapterOpts.backendType = WGPUBackendType_Undefined;
                    wgpuInstanceRequestAdapter(_instance, &adapterOpts, adapterCallbackInfo);
                }
            }
        }
    } else {
        // Default request failed entirely, try D3D11 explicitly
        ydebug("initWebGPU: Default adapter failed, trying D3D11 backend...");
        adapterOpts.backendType = WGPUBackendType_D3D11;
        wgpuInstanceRequestAdapter(_instance, &adapterOpts, adapterCallbackInfo);
    }
#endif

    if (!_adapter) {
        return Err<void>("Failed to get WebGPU adapter");
    }
    ydebug("initWebGPU: Adapter obtained");

    // Log adapter info and check for Null backend
    {
        WGPUAdapterInfo info = {};
        if (wgpuAdapterGetInfo(_adapter, &info) == WGPUStatus_Success) {
            auto sv = [](WGPUStringView s) -> std::string {
                return (s.data && s.length > 0) ? std::string(s.data, s.length) : "(unknown)";
            };
            ydebug("GPU adapter: {} ({})", sv(info.device), sv(info.vendor));
            ydebug("GPU backend: {} | architecture: {} | description: {}",
                  static_cast<int>(info.backendType), sv(info.architecture), sv(info.description));
            ydebug("GPU vendor ID: 0x{:x} | device ID: 0x{:x} | type: {}",
                  info.vendorID, info.deviceID, static_cast<int>(info.adapterType));

#ifdef __ANDROID__
            // On Android, reject Null backend - we need real Vulkan
            if (info.backendType == WGPUBackendType_Null) {
                wgpuAdapterInfoFreeMembers(info);
                wgpuAdapterRelease(_adapter);
                _adapter = nullptr;
                return Err<void>("Vulkan not available - got Null backend. "
                    "Ensure Vulkan driver (e.g., SwiftShader) is properly installed.");
            }
#endif
            wgpuAdapterInfoFreeMembers(info);
        } else {
            ywarn("Failed to query adapter info");
        }
    }

    // Query adapter limits to avoid requesting more than supported
    WGPULimits adapterLimits = {};
    wgpuAdapterGetLimits(_adapter, &adapterLimits);
    uint64_t adapterMaxStorage = adapterLimits.maxStorageBufferBindingSize;
    ydebug("GPU adapter maxStorageBufferBindingSize: {} MB", adapterMaxStorage / (1024 * 1024));

    // Request device with limits capped to adapter support
    WGPULimits limits = {};
    limits.maxTextureDimension1D = WGPU_LIMIT_U32_UNDEFINED;
    // Request higher texture size for large font atlases (CJK support needs >8192)
    limits.maxTextureDimension2D = std::min(16384u, adapterLimits.maxTextureDimension2D);
    limits.maxTextureDimension3D = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxTextureArrayLayers = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxBindGroups = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxBindGroupsPlusVertexBuffers = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxBindingsPerBindGroup = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxDynamicUniformBuffersPerPipelineLayout = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxDynamicStorageBuffersPerPipelineLayout = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxSampledTexturesPerShaderStage = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxSamplersPerShaderStage = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxStorageBuffersPerShaderStage = 10;  // Need 9 for grid shader (vector + coverage fonts)
    limits.maxStorageTexturesPerShaderStage = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxUniformBuffersPerShaderStage = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxUniformBufferBindingSize = WGPU_LIMIT_U64_UNDEFINED;
    limits.maxStorageBufferBindingSize = std::min(static_cast<uint64_t>(512 * 1024 * 1024), adapterMaxStorage);
    limits.minUniformBufferOffsetAlignment = WGPU_LIMIT_U32_UNDEFINED;
    limits.minStorageBufferOffsetAlignment = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxVertexBuffers = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxBufferSize = std::min(static_cast<uint64_t>(1024) * 1024 * 1024, adapterLimits.maxBufferSize);
    limits.maxVertexAttributes = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxVertexBufferArrayStride = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxInterStageShaderVariables = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxColorAttachments = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxColorAttachmentBytesPerSample = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxComputeWorkgroupStorageSize = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxComputeInvocationsPerWorkgroup = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxComputeWorkgroupSizeX = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxComputeWorkgroupSizeY = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxComputeWorkgroupSizeZ = WGPU_LIMIT_U32_UNDEFINED;
    limits.maxComputeWorkgroupsPerDimension = WGPU_LIMIT_U32_UNDEFINED;

    WGPUDeviceDescriptor deviceDesc = {};
    deviceDesc.label = WGPU_STR("yetty device");
    deviceDesc.requiredFeatureCount = 0;
    deviceDesc.requiredLimits = &limits;
    deviceDesc.defaultQueue.label = WGPU_STR("default queue");
    deviceDesc.uncapturedErrorCallbackInfo.callback = [](WGPUDevice const* device, WGPUErrorType type,
                                                         WGPUStringView message, void* userdata1, void* userdata2) {
        auto* self = static_cast<YettyImpl*>(userdata1);
        auto msg = message.data ? std::string(message.data, message.length) : "unknown";
        yerror("WebGPU error ({}): {}", static_cast<int>(type), msg);
        self->_fatalGpuError = true;
        self->_fatalGpuErrorMsg = msg;
    };
    deviceDesc.uncapturedErrorCallbackInfo.userdata1 = this;

    std::string deviceError;
    WGPURequestDeviceCallbackInfo deviceCallbackInfo = {};
    deviceCallbackInfo.mode = WGPUCallbackMode_AllowSpontaneous;
    deviceCallbackInfo.callback = [](WGPURequestDeviceStatus status, WGPUDevice device,
                                     WGPUStringView message, void* userdata1, void* userdata2) {
        if (status == WGPURequestDeviceStatus_Success) {
            *static_cast<WGPUDevice*>(userdata1) = device;
        } else {
            auto msg = message.data ? std::string(message.data, message.length) : "unknown";
            *static_cast<std::string*>(userdata2) = msg;
        }
    };
    deviceCallbackInfo.userdata1 = &_device;
    deviceCallbackInfo.userdata2 = &deviceError;

    ydebug("initWebGPU: Requesting device...");
    wgpuAdapterRequestDevice(_adapter, &deviceDesc, deviceCallbackInfo);

#if defined(__EMSCRIPTEN__)
    // On Emscripten, WebGPU operations are async - poll until callback fires
    while (!_device && deviceError.empty()) {
        emscripten_sleep(1);
    }
#endif

    if (!_device) {
        return Err<void>("Failed to get WebGPU device: " + deviceError);
    }
    ydebug("initWebGPU: Device obtained");

    _queue = wgpuDeviceGetQueue(_device);
    ydebug("initWebGPU: Queue obtained");

    // Configure surface (skip in headless mode)
    if (_surface) {
        WGPUSurfaceCapabilities caps = {};
        wgpuSurfaceGetCapabilities(_surface, _adapter, &caps);
        if (caps.formatCount > 0) {
            _surfaceFormat = caps.formats[0];
            ydebug("Surface format: {}", static_cast<int>(_surfaceFormat));
        }
        wgpuSurfaceCapabilitiesFreeMembers(caps);

        configureSurface(_initialWidth, _initialHeight);
    } else {
        // Headless mode - use default format and set initial dimensions
        _surfaceFormat = WGPUTextureFormat_BGRA8Unorm;
        _surfaceWidth = _initialWidth;
        _surfaceHeight = _initialHeight;
        ydebug("Headless mode: using default surface format BGRA8Unorm, {}x{}", _surfaceWidth, _surfaceHeight);
    }

    ydebug("WebGPU initialized");
    return Ok();
}

void YettyImpl::configureSurface(uint32_t width, uint32_t height) noexcept {
    // Skip if no surface (headless mode)
    if (!_surface) {
        _surfaceWidth = width;
        _surfaceHeight = height;
        return;
    }

    // Release any held texture/view before reconfiguring - they become invalid
    if (_currentTextureView) {
        wgpuTextureViewRelease(_currentTextureView);
        _currentTextureView = nullptr;
    }
    if (_currentTexture) {
        wgpuTextureRelease(_currentTexture);
        _currentTexture = nullptr;
    }
#if defined(__EMSCRIPTEN__)
    // On web: DO NOT release pending textures here - GPU might still be using them!
    // The GPU callback will release them when it fires (or they'll be orphaned but that's OK)
    // Just clear our references so we don't double-release
    _pendingReleaseTextureView = nullptr;
    _pendingReleaseTexture = nullptr;
#endif

    _surfaceWidth = width;
    _surfaceHeight = height;

    WGPUSurfaceConfiguration config = {};
    config.device = _device;
    config.format = _surfaceFormat;
    config.usage = WGPUTextureUsage_RenderAttachment;
    config.width = width;
    config.height = height;
    config.presentMode = WGPUPresentMode_Fifo;
    config.alphaMode = WGPUCompositeAlphaMode_Auto;

    wgpuSurfaceConfigure(_surface, &config);
}

Result<WGPUTextureView> YettyImpl::getCurrentTextureView() noexcept {
    // No surface in headless mode
    if (!_surface) {
        return Err<WGPUTextureView>("No surface in headless mode");
    }

    // Return cached texture view if available (caching within single frame)
    // On web: present() clears _currentTextureView, so next frame gets new texture
    // On native: present() also clears it after wgpuSurfacePresent
    if (_currentTextureView) {
        return Ok(_currentTextureView);
    }

    WGPUSurfaceTexture surfaceTexture = {};
    wgpuSurfaceGetCurrentTexture(_surface, &surfaceTexture);

    if (surfaceTexture.status != WGPUSurfaceGetCurrentTextureStatus_SuccessOptimal &&
        surfaceTexture.status != WGPUSurfaceGetCurrentTextureStatus_SuccessSuboptimal) {
        return Err<WGPUTextureView>("Failed to get surface texture");
    }

    _currentTexture = surfaceTexture.texture;

    WGPUTextureViewDescriptor viewDesc = {};
    viewDesc.format = _surfaceFormat;
    viewDesc.dimension = WGPUTextureViewDimension_2D;
    viewDesc.mipLevelCount = 1;
    viewDesc.arrayLayerCount = 1;

    _currentTextureView = wgpuTextureCreateView(_currentTexture, &viewDesc);
    return Ok(_currentTextureView);
}

void YettyImpl::present() noexcept {
    // No surface in headless mode
    if (!_surface) {
        return;
    }

#if !defined(__EMSCRIPTEN__)
    // On native: release texture references before presenting
    // wgpuSurfacePresent ensures GPU has finished using the texture
    if (_currentTextureView) {
        wgpuTextureViewRelease(_currentTextureView);
        _currentTextureView = nullptr;
    }
    if (_currentTexture) {
        wgpuTextureRelease(_currentTexture);
        _currentTexture = nullptr;
    }
    wgpuSurfacePresent(_surface);
#else
    // On Emscripten: move texture to "pending release" - will be released in GPU-done callback
    // This ensures GPU has finished using the texture before we release it
    // Note: if there's already a pending texture, release it first (shouldn't happen normally)
    if (_pendingReleaseTextureView) {
        wgpuTextureViewRelease(_pendingReleaseTextureView);
    }
    if (_pendingReleaseTexture) {
        wgpuTextureRelease(_pendingReleaseTexture);
    }
    _pendingReleaseTextureView = _currentTextureView;
    _pendingReleaseTexture = _currentTexture;
    _currentTextureView = nullptr;
    _currentTexture = nullptr;
#endif
}

