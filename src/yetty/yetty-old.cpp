void YettyImpl::initEventLoop() noexcept {
    auto loop = *base::EventLoop::instance();

    // Frame render timer - configurable FPS (default 60)
    // On desktop: libuv timer, on webasm: emscripten timer via tick()
    auto frameTimerResult = loop->createTimer();
    if (!frameTimerResult) {
        yerror("Failed to create frame timer: {}", error_msg(frameTimerResult));
        return;
    }
    _frameTimerId = *frameTimerResult;
    // VNC client mode: render on server frames only, use slow timer (1fps) for status/reconnect
    // Normal mode: 60 FPS timer drives rendering
    uint32_t timerIntervalMs = _vncClientMode ? 1000 : 16;
    if (auto res = loop->configTimer(_frameTimerId, timerIntervalMs); !res) {
        yerror("Failed to configure frame timer: {}", error_msg(res));
        return;
    }
    auto listener = sharedAs<base::EventListener>();
    ydebug("initEventLoop: listener={}", static_cast<void*>(listener.get()));
    if (auto res = loop->registerTimerListener(_frameTimerId, listener); !res) {
        yerror("Failed to register frame timer listener: {}", error_msg(res));
        return;
    }

    // Register for input events (platform-specific sources: GLFW, Emscripten, Android)
    loop->registerListener(base::Event::Type::KeyDown, sharedAs<base::EventListener>());
    loop->registerListener(base::Event::Type::KeyUp, sharedAs<base::EventListener>());
    loop->registerListener(base::Event::Type::Char, sharedAs<base::EventListener>());
    loop->registerListener(base::Event::Type::MouseDown, sharedAs<base::EventListener>());
    loop->registerListener(base::Event::Type::MouseUp, sharedAs<base::EventListener>());
    loop->registerListener(base::Event::Type::MouseMove, sharedAs<base::EventListener>());
    loop->registerListener(base::Event::Type::Scroll, sharedAs<base::EventListener>());
    loop->registerListener(base::Event::Type::Resize, sharedAs<base::EventListener>());

    // Register for Copy events to write to system clipboard
    loop->registerListener(base::Event::Type::Copy, sharedAs<base::EventListener>());
    // Register for SetCursor events from GPUScreen
    loop->registerListener(base::Event::Type::SetCursor, sharedAs<base::EventListener>());
    // Register for SetFrameRate events (OSC 666671)
    loop->registerListener(base::Event::Type::SetFrameRate, sharedAs<base::EventListener>());
    // Register for ScreenUpdate events (PTY activity)
    loop->registerListener(base::Event::Type::ScreenUpdate, sharedAs<base::EventListener>());

    // Start the frame timer - each platform's EventLoop handles this appropriately
    // Desktop: libuv timer, Android: timerfd+ALooper, WebASM: emscripten timer
    if (auto res = loop->startTimer(_frameTimerId); !res) {
        yerror("Failed to start frame timer: {}", error_msg(res));
        return;
    }

    ydebug("Event loop initialized");
}

void YettyImpl::shutdownEventLoop() noexcept {
    auto loop = *base::EventLoop::instance();
    if (_frameTimerId >= 0) {
        loop->destroyTimer(_frameTimerId);
        _frameTimerId = -1;
    }
}

Result<bool> YettyImpl::onEvent(const base::Event& event) {
    ydebug("onEvent: type={} timerId={} frameTimerId={}", static_cast<int>(event.type), event.timer.timerId, _frameTimerId);
    // Track modifier key state for scroll events
    // GLFW-compatible key codes: GLFW_KEY_LEFT_CONTROL=341, RIGHT=345, LEFT_SHIFT=340, RIGHT=344
    if (event.type == base::Event::Type::KeyDown) {
        int key = event.key.key;
        if (key == 341 || key == 345) _ctrlPressed = true;
        if (key == 340 || key == 344) _shiftPressed = true;
        return Ok(false);  // Let workspace handle
    }
    if (event.type == base::Event::Type::KeyUp) {
        int key = event.key.key;
        if (key == 341 || key == 345) _ctrlPressed = false;
        if (key == 340 || key == 344) _shiftPressed = false;
        return Ok(false);
    }

    // Mouse move: track position for scroll/button events
    if (event.type == base::Event::Type::MouseMove) {
        _lastMouseX = event.mouse.x;
        _lastMouseY = event.mouse.y;
        return Ok(false);  // Let workspace handle
    }

    // Resize event from platform InputManager
    if (event.type == base::Event::Type::Resize) {
        handleResize(static_cast<int>(event.resize.width),
                    static_cast<int>(event.resize.height));
        return Ok(true);
    }

    // Frame timer or ScreenUpdate: render
    if ((event.type == base::Event::Type::Timer && event.timer.timerId == _frameTimerId) ||
        event.type == base::Event::Type::ScreenUpdate) {
        ydebug("onEvent: frame timer fired, calling mainLoopIteration");
        // Check if window should close
        if (!_vncHeadless) {
            if (auto surfaceResult = SurfaceManager::instance(); surfaceResult && (*surfaceResult)->shouldClose()) {
                ydebug("Window close requested, stopping event loop");
                (*base::EventLoop::instance())->stop();
                return Ok(true);
            }
        }
        // Process GPU events so async callbacks (like render done) can fire
        wgpuInstanceProcessEvents(_instance);

        if (auto res = mainLoopIteration(); !res) {
            yerror("Fatal render error: {}", error_msg(res));
            (*base::EventLoop::instance())->stop();
        }
        return Ok(true);
    }

    // SetCursor event: change mouse cursor shape
    if (event.type == base::Event::Type::SetCursor && !_vncHeadless) {
        if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
            int shape = event.setCursor.shape;
            // Map cursor constants to CursorType
            CursorType cursorType = CursorType::Arrow;
            switch (shape) {
                case 0: cursorType = CursorType::Arrow; break;
                case 0x00036002: cursorType = CursorType::IBeam; break;
                case 0x00036004: cursorType = CursorType::Hand; break;
                case 0x00036005: cursorType = CursorType::ResizeH; break;
                case 0x00036006: cursorType = CursorType::ResizeV; break;
                default: cursorType = CursorType::Arrow; break;
            }
            (*surfaceResult)->setCursor(cursorType);
        }
        return Ok(true);
    }

    // Copy event: write selected text to system clipboard
    if (event.type == base::Event::Type::Copy && event.payload && !_vncHeadless) {
        if (auto clipboardResult = ClipboardManager::instance(); clipboardResult) {
            auto text = std::static_pointer_cast<std::string>(event.payload);
            if (text && !text->empty()) {
                (*clipboardResult)->setText(*text);
                ydebug("Clipboard: copied {} bytes", text->size());
            }
        }
        return Ok(true);
    }

    // SetFrameRate event: reconfigure frame timer (OSC 666671)
    if (event.type == base::Event::Type::SetFrameRate) {
        uint32_t fps = event.setFrameRate.fps;
        uint32_t intervalMs = (fps > 0) ? (1000 / fps) : 16;
        if (intervalMs < 1) intervalMs = 1;
        auto loop = *base::EventLoop::instance();
        if (auto res = loop->configTimer(_frameTimerId, intervalMs); !res) {
            yerror("Failed to reconfigure frame timer: {}", error_msg(res));
        } else {
            ydebug("Frame rate changed to {} FPS ({}ms interval)", fps, intervalMs);
        }
        return Ok(true);
    }

    return Ok(false);
}

static void signalHandler(int sig) {
    ydebug("Received signal {}, shutting down...", sig);
    (void)(*base::EventLoop::instance())->stop();
}

Result<void> YettyImpl::run() noexcept {
    ydebug("run: starting");

    // Start async operations in workspaces (propagates to terminals -> SSH)
    // This must happen before loop->start() so SSH poll handlers are registered
    for (auto& ws : _workspaces) {
        if (auto r = ws->run(); !r) {
            return Err<void>("Failed to run workspace", r);
        }
    }

    // Now start the event loop - SSH poll events will fire
    std::signal(SIGINT, signalHandler);
    std::signal(SIGTERM, signalHandler);
    auto loop = *base::EventLoop::instance();
    loop->start();

    ydebug("Run finished.");

    if (_fatalGpuError) {
        return Err<void>("Fatal GPU error: " + _fatalGpuErrorMsg);
    }
    return Ok();
}

Result<void> YettyImpl::iterate() noexcept {
    return mainLoopIteration();
}

Result<void> YettyImpl::mainLoopIteration() noexcept {
    if (_fatalGpuError) {
        return Err<void>("Fatal GPU error: " + _fatalGpuErrorMsg);
    }

    // Process GPU events so async callbacks (like render done) can fire
    wgpuInstanceProcessEvents(_instance);

#if YETTY_WEB
    // Web builds: drain EventQueue since libuv async wakeup is not available
    // GPU callbacks may have pushed events that need to be processed
    if (_eventQueue) {
        _eventQueue->drain();
    }

    // VNC client reconnection logic
    if (_vncClientMode && _vncClient && !_vncClient->isConnected() && !_vncClient->_connecting) {
        auto now = std::chrono::steady_clock::now();
        auto elapsed = std::chrono::duration_cast<std::chrono::milliseconds>(now - _vncLastReconnectAttempt).count();
        if (elapsed >= VNC_RECONNECT_INTERVAL_MS) {
            _vncLastReconnectAttempt = now;
            ydebug("VNC client attempting reconnection...");
            if (auto res = _vncClient->reconnect(); !res) {
                ywarn("VNC reconnection failed: {} - will retry in {}ms", res.error().message(), VNC_RECONNECT_INTERVAL_MS);
            }
        }
    }

    // Telnet client reconnection is handled by TelnetPtyReader/TelnetClient automatically
#endif

    // Apply deferred resize before acquiring the surface texture
    if (_pendingResize) {
        _pendingResize = false;
        if (!_vncHeadless) {
            configureSurface(_pendingResizeW, _pendingResizeH);
        }
        if (_yettyContext.yguiOverlay) {
            _yettyContext.yguiOverlay->updateDisplaySize(_pendingResizeW, _pendingResizeH);
        }
        if (_activeWorkspace && !_vncHeadless) {
            float statusbarHeight = _yettyContext.yguiOverlay ? _yettyContext.yguiOverlay->getStatusbarHeight() : 0.0f;
            float wsH = std::max(1.0f, static_cast<float>(_pendingResizeH) - statusbarHeight);
            _activeWorkspace->resize(static_cast<float>(_pendingResizeW), wsH);
        }
        // VNC client mode: send resize to server (was deferred because _inRender was true)
        if (_vncClientMode && _vncClient && _pendingResizeW > 0 && _pendingResizeH > 0) {
            float statusbarH = _yettyContext.yguiOverlay ? _yettyContext.yguiOverlay->getStatusbarHeight() : 0.0f;
            int vncH = static_cast<int>(_pendingResizeH) - static_cast<int>(statusbarH);
            if (vncH > 0) {
                _vncClient->sendResize(static_cast<uint16_t>(_pendingResizeW), static_cast<uint16_t>(vncH));
                ydebug("VNC client sent deferred resize: {}x{}", _pendingResizeW, vncH);
            }
        }
    }

    // In headless mode with no client connected, skip entire render iteration
    // to avoid accumulating GPU operations that leak NVIDIA FDs
    if (_vncHeadless && _vncServerMode) {
        bool vncClientReady = _vncRequestedWidth > 0 && _vncRequestedHeight > 0;
        if (!vncClientReady) {
            return Ok();  // No client yet, nothing to render
        }
    }

#if defined(__EMSCRIPTEN__)
    // On web: set _inRender BEFORE getting texture to protect it from resize events
    // Resize events check _inRender and defer if true
    // Without this, a resize between getCurrentTextureView and the later _inRender=true
    // would destroy the texture we just acquired
    if (_inRender) {
        // Already rendering - remember we need another update
        _nextScreenUpdateNeeded = true;
        return Ok();
    }
    _inRender = true;  // Protect texture from resize events
#endif

    // In headless mode or when surface is unavailable, skip surface operations
    WGPUTextureView targetView = nullptr;
    if (!_vncHeadless && _surface) {
        auto viewResult = getCurrentTextureView();
        if (!viewResult) {
#if defined(__EMSCRIPTEN__)
            _inRender = false;  // Failed to get texture, allow resize
#endif
            return Err<void>("Failed to get texture view");
        }
        targetView = *viewResult;
    }

    // Update shared uniforms
    double now = 0.0;
    if (!_vncHeadless) {
        if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
            now = (*surfaceResult)->getTime();
        }
    } else {
        // Headless mode: use monotonic clock
        static auto startTime = std::chrono::steady_clock::now();
        auto elapsed = std::chrono::steady_clock::now() - startTime;
        now = std::chrono::duration<double>(elapsed).count();
    }
    static double lastTime = now;
    float deltaTime = static_cast<float>(now - lastTime);
    lastTime = now;

    int windowWidth, windowHeight;
    if (_vncHeadless) {
        // Headless mode: use VNC requested size or default (no window)
        windowWidth = _vncRequestedWidth > 0 ? static_cast<int>(_vncRequestedWidth) : static_cast<int>(_initialWidth);
        windowHeight = _vncRequestedHeight > 0 ? static_cast<int>(_vncRequestedHeight) : static_cast<int>(_initialHeight);
    } else {
        windowWidth = windowHeight = 0;
        if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
            (*surfaceResult)->getWindowSize(windowWidth, windowHeight);
        }
    }

    _sharedUniforms.time = static_cast<float>(now);
    _sharedUniforms.deltaTime = deltaTime;
    _sharedUniforms.screenWidth = static_cast<float>(windowWidth);
    _sharedUniforms.screenHeight = static_cast<float>(windowHeight);
    _sharedUniforms.mouseX = _lastMouseX;
    _sharedUniforms.mouseY = _lastMouseY;
    wgpuQueueWriteBuffer(_queue, _sharedUniformBuffer, 0, &_sharedUniforms, sizeof(SharedUniforms));

    // Upload any pending font glyphs (e.g., bold/italic loaded on demand)
    if (_yettyContext.fontManager) {
        if (auto msdfFont = _yettyContext.fontManager->getDefaultMsMsdfFont()) {
            if (msdfFont->atlas()->hasPendingGlyphs()) {
                auto uploadResult = msdfFont->atlas()->uploadPendingGlyphs(_device, _queue);
                if (!uploadResult) {
                    ywarn("Failed to upload pending glyphs: {}", uploadResult.error().message());
                }
            }
        }
    }

    // === CAPTURE/VNC SERVER MODE ===
    // Render to offscreen texture for capture benchmark or VNC streaming
    // In headless mode, always capture (since there's no local display)
    // VNC: throttle to max 30 FPS to avoid 100% CPU from ScreenUpdate spam
    static auto lastVncFrame = std::chrono::steady_clock::now();
    auto vncNow = std::chrono::steady_clock::now();
    bool vncThrottleOk = std::chrono::duration_cast<std::chrono::milliseconds>(vncNow - lastVncFrame).count() >= 33; // 30 FPS max

    // VNC SERVER PROTOCOL: Only start sending frames AFTER client sends its resize
    // _vncRequestedWidth/Height are set by onResize callback when client's resize arrives
    bool vncClientReady = _vncRequestedWidth > 0 && _vncRequestedHeight > 0;
    // Flow control: don't capture if VNC server isn't ready (state machine busy or waiting for ack)
    bool vncServerReady = !_vncServer || _vncServer->isReadyForFrame();
    // Check if dimensions changed - if so, we MUST capture with new dimensions immediately
    bool vncDimensionsChanged = _vncServerMode && vncClientReady &&
        (_captureWidth != _vncRequestedWidth || _captureHeight != _vncRequestedHeight);
    // Always capture when there are clients - state machine handles diff detection
    // When dimensions changed, override vncServerReady - old state machine will be reset in sendFrame()
    bool doCapture = (_captureBenchmark && (++_captureSkipCounter % CAPTURE_EVERY_N_FRAMES == 0)) ||
                     (_vncHeadless && _vncServerMode && _vncServer && vncClientReady && (vncServerReady || vncDimensionsChanged)) ||
                     (_vncServerMode && _vncServer && _vncServer->hasClients() && vncClientReady && vncThrottleOk && (vncServerReady || vncDimensionsChanged));

    // Debug: track capture vs skip ratio
    static uint32_t captureCount = 0, skipCount = 0;
    static double lastDbgTime = 0;
    if (_vncHeadless) {
        if (doCapture) captureCount++; else skipCount++;
        if (now - lastDbgTime >= 1.0) {
            ydebug("VNC STATS: captures={} skips={} ackReady={}", captureCount, skipCount, vncServerReady);
            captureCount = skipCount = 0;
            lastDbgTime = now;
        }
    }
    // Determine capture size: use VNC requested size if set, otherwise window size
    uint32_t captureW = (_vncServerMode && _vncRequestedWidth > 0) ? _vncRequestedWidth : static_cast<uint32_t>(windowWidth);
    uint32_t captureH = (_vncServerMode && _vncRequestedHeight > 0) ? _vncRequestedHeight : static_cast<uint32_t>(windowHeight);

#if !defined(__EMSCRIPTEN__)
    // Skip GPU work if already rendering, but remember we need another update
    // On web, this check is done earlier (before getCurrentTextureView) to protect texture
    if (_inRender) {
        auto t = std::chrono::high_resolution_clock::now();
        auto ms = std::chrono::duration_cast<std::chrono::milliseconds>(t.time_since_epoch()).count();
        ydebug("[TIME] SKIPPING GPU at {}ms (_inRender=true), setting _nextScreenUpdateNeeded", ms);
        _nextScreenUpdateNeeded = true;
        return Ok();
    }

    // Mark rendering in progress — cleared by wgpuQueueOnSubmittedWorkDone callback
    _inRender = true;
#endif

    auto t = std::chrono::high_resolution_clock::now();
    auto ms = std::chrono::duration_cast<std::chrono::milliseconds>(t.time_since_epoch()).count();
    ydebug("[TIME] STARTING GPU at {}ms", ms);

    if (doCapture && captureW > 0 && captureH > 0) {
        // Ensure capture resources match requested size
        if (auto res = ensureCaptureResources(captureW, captureH); !res) {
            _inRender = false;
            return Err<void>("Failed to ensure capture resources", res);
        }

        // Render to capture texture
        WGPUCommandEncoderDescriptor encoderDesc = {};
        WGPUCommandEncoder captureEncoder = wgpuDeviceCreateCommandEncoder(_device, &encoderDesc);
        if (!captureEncoder) {
            _inRender = false;
            return Err<void>("Failed to create capture command encoder");
        }

        WGPURenderPassColorAttachment captureAttachment = {};
        captureAttachment.view = _captureTextureView;
        captureAttachment.loadOp = WGPULoadOp_Clear;
        captureAttachment.storeOp = WGPUStoreOp_Store;
        captureAttachment.clearValue = {0.1f, 0.1f, 0.2f, 1.0f};
        captureAttachment.depthSlice = WGPU_DEPTH_SLICE_UNDEFINED;

        WGPURenderPassDescriptor capturePassDesc = {};
        capturePassDesc.colorAttachmentCount = 1;
        capturePassDesc.colorAttachments = &captureAttachment;

        WGPURenderPassEncoder capturePass = wgpuCommandEncoderBeginRenderPass(captureEncoder, &capturePassDesc);
        if (capturePass) {
            // CRITICAL: Update render target dimensions for capture texture
            // The terminal's scissor rect is clamped to these values - without this,
            // the last rows may be clipped when capture size != window size
            _yettyContext.gpu.renderTargetWidth = captureW;
            _yettyContext.gpu.renderTargetHeight = captureH;

            // CRITICAL: In non-headless VNC mode, workspace may be sized to window dimensions
            // but we need to render at VNC capture dimensions. Resize workspace temporarily.
            float savedWorkspaceW = 0.0f;
            float savedWorkspaceH = 0.0f;
            bool workspaceResized = false;
            if (_activeWorkspace && !_vncHeadless && _vncServerMode) {
                savedWorkspaceW = _activeWorkspace->width();
                savedWorkspaceH = _activeWorkspace->height();
                if (savedWorkspaceW != static_cast<float>(captureW) ||
                    savedWorkspaceH != static_cast<float>(captureH)) {
                    _activeWorkspace->resize(static_cast<float>(captureW), static_cast<float>(captureH));
                    workspaceResized = true;
                }
            }

            if (_activeWorkspace) {
                if (auto res = _activeWorkspace->render(capturePass); !res) {
                    // Restore workspace size before returning error
                    if (workspaceResized) {
                        _activeWorkspace->resize(savedWorkspaceW, savedWorkspaceH);
                    }
                    wgpuRenderPassEncoderEnd(capturePass);
                    wgpuRenderPassEncoderRelease(capturePass);
                    wgpuCommandEncoderRelease(captureEncoder);
                    _inRender = false;
                    return Err<void>("Capture workspace render failed", res);
                }
            }
            // Render UI overlay (statusbar) for both capture benchmark and VNC streaming
            // VNC client will see server's statusbar, plus its own statusbar on top
            if (_yettyContext.yguiOverlay) {
                if (auto res = _yettyContext.yguiOverlay->render(capturePass); !res) {
                    // Restore workspace size before returning error
                    if (workspaceResized) {
                        _activeWorkspace->resize(savedWorkspaceW, savedWorkspaceH);
                    }
                    wgpuRenderPassEncoderEnd(capturePass);
                    wgpuRenderPassEncoderRelease(capturePass);
                    wgpuCommandEncoderRelease(captureEncoder);
                    _inRender = false;
                    return Err<void>("Capture overlay render failed", res);
                }
            }
            wgpuRenderPassEncoderEnd(capturePass);
            wgpuRenderPassEncoderRelease(capturePass);

            // Restore workspace size for local rendering
            if (workspaceResized) {
                _activeWorkspace->resize(savedWorkspaceW, savedWorkspaceH);
            }
        }

        // Perform capture benchmark (copy to buffer, map, compress)
        if (_captureBenchmark) {
            // Note: performFrameCapture finishes and submits the encoder
            if (auto res = performFrameCapture(captureEncoder); !res) {
                // Non-fatal - log warning and continue
                ywarn("Frame capture failed: {}", res.error().message());
            }
        } else {
            // Just finish and submit for VNC server
            WGPUCommandBufferDescriptor cmdDesc = {};
            WGPUCommandBuffer cmdBuf = wgpuCommandEncoderFinish(captureEncoder, &cmdDesc);
            if (cmdBuf) {
                wgpuQueueSubmit(_queue, 1, &cmdBuf);
                wgpuCommandBufferRelease(cmdBuf);
            }
            wgpuCommandEncoderRelease(captureEncoder);
        }

        // VNC server: ALWAYS call sendFrame after rendering to capture texture
        // The state machine inside sendFrame handles when to start new diff detection
        if (_vncServerMode && _vncServer) {
            if (auto res = _vncServer->sendFrame(_captureTexture, captureW, captureH); !res) {
                ywarn("VNC send failed: {}", res.error().message());
            }
            // Process pending GPU callbacks so VNC state machine advances
            wgpuInstanceProcessEvents(_instance);
            lastVncFrame = vncNow;
        }
    } else if (_vncServerMode && _vncServer && vncClientReady && _captureTexture && _captureWidth > 0 && _captureHeight > 0) {
        // No capture this frame - only advance state machine if NOT idle
        // If idle, don't call sendFrame or we'll compare stale content!
        // IMPORTANT: Also skip if dimensions changed - let old state machine die,
        // next doCapture=true will start fresh with new dimensions
        bool dimensionsChanged = _captureWidth != _vncRequestedWidth || _captureHeight != _vncRequestedHeight;
        if (!_vncServer->isReadyForFrame() && !dimensionsChanged) {
            if (auto res = _vncServer->sendFrame(_captureTexture, _captureWidth, _captureHeight); !res) {
                ywarn("VNC send failed: {}", res.error().message());
            }
            wgpuInstanceProcessEvents(_instance);
        }
    }

    // === NORMAL RENDER TO SURFACE (skip in headless mode or when no surface) ===
    if (!_vncHeadless && _surface) {
        WGPUCommandEncoderDescriptor encoderDesc = {};
        WGPUCommandEncoder encoder = wgpuDeviceCreateCommandEncoder(_device, &encoderDesc);
        if (!encoder) {
            _inRender = false;
            return Err<void>("Failed to create command encoder");
        }

        WGPURenderPassColorAttachment colorAttachment = {};
        colorAttachment.view = targetView;
        colorAttachment.loadOp = WGPULoadOp_Clear;
        colorAttachment.storeOp = WGPUStoreOp_Store;
        colorAttachment.clearValue = {0.1f, 0.1f, 0.2f, 1.0f};
        colorAttachment.depthSlice = WGPU_DEPTH_SLICE_UNDEFINED;

        WGPURenderPassDescriptor passDesc = {};
        passDesc.colorAttachmentCount = 1;
        passDesc.colorAttachments = &colorAttachment;

        WGPURenderPassEncoder pass = wgpuCommandEncoderBeginRenderPass(encoder, &passDesc);
        if (pass) {
            // Update render target dimensions for scissor rect clamping during resize
            _yettyContext.gpu.renderTargetWidth = _surfaceWidth;
            _yettyContext.gpu.renderTargetHeight = _surfaceHeight;

            // VNC client mode: render received frame
            if (_vncClientMode && _vncClient) {
                // Update texture with any received tiles
                if (auto res = _vncClient->updateTexture(); !res) {
                    ywarn("VNC texture update failed: {}", res.error().message());
                }
                // Update statusbar with connection stats
                if (_yettyContext.yguiOverlay) {
                    const auto& stats = _vncClient->stats();
                    char buf[128];
                    snprintf(buf, sizeof(buf), "%s:%u q=%u fps=%.1f tps=%.0f %.2fMb/s",
                             _vncClient->serverHost().c_str(), _vncClient->serverPort(),
                             stats.quality, stats.fps, stats.tps, stats.mbps);
                    _yettyContext.yguiOverlay->setStatusText(buf);
                }
                // Render fullscreen quad with frame texture
                // Pass render target dimensions to clamp scissor during resize race condition
                if (auto res = _vncClient->render(pass, _surfaceWidth, _surfaceHeight); !res) {
                    wgpuRenderPassEncoderEnd(pass);
                    wgpuRenderPassEncoderRelease(pass);
                    wgpuCommandEncoderRelease(encoder);
                    _inRender = false;
                    return Err<void>("VNC client render failed", res);
                }
            } else if (_activeWorkspace) {
                if (auto res = _activeWorkspace->render(pass); !res) {
                    wgpuRenderPassEncoderEnd(pass);
                    wgpuRenderPassEncoderRelease(pass);
                    wgpuCommandEncoderRelease(encoder);
                    _inRender = false;
                    return Err<void>("Workspace render failed", res);
                }
            }
            // Render overlay (statusbar, context menus, dialogs) after main content
            if (_yettyContext.yguiOverlay) {
                if (auto res = _yettyContext.yguiOverlay->render(pass); !res) {
                    ywarn("YGuiOverlay render failed: {}", res.error().message());
                }
            }
            wgpuRenderPassEncoderEnd(pass);
            wgpuRenderPassEncoderRelease(pass);
        }

        WGPUCommandBufferDescriptor cmdDesc = {};
        WGPUCommandBuffer cmdBuffer = wgpuCommandEncoderFinish(encoder, &cmdDesc);
        if (cmdBuffer) {
            wgpuQueueSubmit(_queue, 1, &cmdBuffer);
            wgpuCommandBufferRelease(cmdBuffer);
        }
        wgpuCommandEncoderRelease(encoder);

        present();
    }

    // Check if GPU error occurred during this frame
    if (_fatalGpuError) {
        _inRender = false;
        return Err<void>("GPU error during frame: " + _fatalGpuErrorMsg);
    }

    // In headless VNC mode, FPS reflects actual frames captured/sent (throttled to ~30 FPS)
    // In normal mode, FPS reflects all render iterations
    if (!_vncHeadless || doCapture) {
        _frameCount++;
    }
    if (now - _lastFpsTime >= 1.0) {
        ydebug("FPS: {}", _frameCount);
        if (_yettyContext.yguiOverlay) {
            _yettyContext.yguiOverlay->setFps(_frameCount);
        }
        _frameCount = 0;
        _lastFpsTime = now;
    }

    // _inRender cleared by wgpuQueueOnSubmittedWorkDone callback when GPU finishes
    WGPUQueueWorkDoneCallbackInfo renderDoneCb = {};
    renderDoneCb.mode = WGPUCallbackMode_AllowSpontaneous;
    renderDoneCb.callback = [](WGPUQueueWorkDoneStatus, WGPUStringView, void* ud1, void*) {
        auto t = std::chrono::high_resolution_clock::now();
        auto ms = std::chrono::duration_cast<std::chrono::milliseconds>(t.time_since_epoch()).count();
        ydebug("[TIME] GPU DONE callback at {}ms", ms);

        auto* self = static_cast<YettyImpl*>(ud1);
        self->_inRender = false;

#if defined(__EMSCRIPTEN__)
        // Web: NOW it's safe to release the texture - GPU has finished using it
        if (self->_pendingReleaseTextureView) {
            wgpuTextureViewRelease(self->_pendingReleaseTextureView);
            self->_pendingReleaseTextureView = nullptr;
        }
        if (self->_pendingReleaseTexture) {
            wgpuTextureRelease(self->_pendingReleaseTexture);
            self->_pendingReleaseTexture = nullptr;
        }
#endif

        // If ScreenUpdate was needed while rendering, dispatch it now
        if (self->_nextScreenUpdateNeeded.exchange(false)) {
            ydebug("[TIME] GPU DONE: _nextScreenUpdateNeeded was true, dispatching ScreenUpdate");
#if !YETTY_WEB
            // Wake up main thread via EventQueue (thread-safe for GPU callbacks)
            // On WebASM, don't push - the main loop already runs at 60fps via requestAnimationFrame
            // and pushing here causes infinite recursion since callbacks fire synchronously
            self->_eventQueue->push(base::Event::screenUpdateEvent());
#endif
        }
    };
    renderDoneCb.userdata1 = this;
    wgpuQueueOnSubmittedWorkDone(_queue, renderDoneCb);
    wgpuInstanceProcessEvents(_instance);

    return Ok();
}

void YettyImpl::handleResize(int newWidth, int newHeight) noexcept {
    if (newWidth == 0 || newHeight == 0) return;

    // Update content scale (framebuffer / window)
    int windowWidth = newWidth, windowHeight = newHeight;
    if (!_vncHeadless) {
        if (auto surfaceResult = SurfaceManager::instance(); surfaceResult) {
            (*surfaceResult)->getWindowSize(windowWidth, windowHeight);
        }
    }
    if (windowWidth > 0 && windowHeight > 0) {
        _contentScaleX = static_cast<float>(newWidth) / static_cast<float>(windowWidth);
        _contentScaleY = static_cast<float>(newHeight) / static_cast<float>(windowHeight);
        ydebug("Content scale updated: {}x{} (fb {}x{} / win {}x{})",
              _contentScaleX, _contentScaleY, newWidth, newHeight, windowWidth, windowHeight);
    }

    // Convert framebuffer pixels to logical coordinates
    // Dawn/Metal handles Retina scaling internally via contentsScale
    float logicalW = (windowWidth > 0) ? static_cast<float>(windowWidth) : static_cast<float>(newWidth);
    float logicalH = (windowHeight > 0) ? static_cast<float>(windowHeight) : static_cast<float>(newHeight);

    uint32_t w = static_cast<uint32_t>(logicalW);
    uint32_t h = static_cast<uint32_t>(logicalH);

    if (_inRender) {
        // Defer — reconfiguring surface now would destroy the in-flight texture
        _pendingResize = true;
        _pendingResizeW = w;
        _pendingResizeH = h;
        return;
    }

    // Safe to reconfigure immediately
    configureSurface(w, h);

    if (_yettyContext.yguiOverlay) {
        _yettyContext.yguiOverlay->updateDisplaySize(w, h);
    }

    // In VNC headless mode, workspace size is controlled by VNC client, not local window
    if (_activeWorkspace && !_vncHeadless) {
        float statusbarHeight = _yettyContext.yguiOverlay ? _yettyContext.yguiOverlay->getStatusbarHeight() : 0.0f;
        float wsH = std::max(1.0f, logicalH - statusbarHeight);
        _activeWorkspace->resize(logicalW, wsH);
    }

    // VNC client mode: tell server about our VNC content area (window minus our statusbar)
    // Server renders its full frame (terminal + server's statusbar) into this area
    if (_vncClientMode && _vncClient && windowWidth > 0 && windowHeight > 0) {
        float statusbarH = _yettyContext.yguiOverlay ? _yettyContext.yguiOverlay->getStatusbarHeight() : 0.0f;
        int vncH = windowHeight - static_cast<int>(statusbarH);
        if (vncH > 0) {
            _vncClient->sendResize(static_cast<uint16_t>(windowWidth), static_cast<uint16_t>(vncH));
            ydebug("VNC client sent resize: {}x{}", windowWidth, vncH);
        }
    }
}

